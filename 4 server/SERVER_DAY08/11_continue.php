<?php
/*
使用continue关键字：输出1/2/3/4/6/7/8/9/11/12/13/14/16...99
提示：输出时跳过能被5整除的数
*/
for($i=1; $i<100; $i++){
	if($i%5===0){
		continue;
	}
	echo  "$i ";
}

echo '<hr>';
/*
练习：输出1+2+5+7+10+11+13+14+17.......98
提示：计算累加和，跳过所有能被3或4整除的数
*/
for($i=1, $sum=0;  $i<=100;  $i++){
	if($i%3===0 || $i%4===0){
		continue;
	}
	$sum += $i;
}

echo $sum;