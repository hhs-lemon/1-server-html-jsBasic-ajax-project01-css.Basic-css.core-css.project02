<?php
/*
练习：计算1*2*3*4*...*100的累乘积，要求如果中间$chengJi超过21747483647，立即终止执行，输出此时的乘积值，以及$i的值
*/
for($i=1,$chengJi=1;  $i<=100;  $i++){
	$chengJi *= $i;
	if($chengJi>=2147483647){
		break;		#退出当前循环到循环外面
	}
}
echo "$i - $chengJi";
echo '<hr>';
/*
练习：输出本世纪所有的闰年，输出够10个即不再输出。
*/
$count = 0;    #已经输出的闰年的个数
for($i=2000;  $i<=2100;  $i++){
	if( ($i%400===0)||(($i%4===0)&&($i%100!==0))){
		echo "$i ";
		$count++;  #又输出了一个闰年
		if($count===10){ 
			break; #已满10个,退出循环
		}
	}
}