<?php
/*
练习7)：输出1个※
*/
echo '※';
echo '<hr>';
/*
练习8)：输出10个※，最后再加1个<br>		
  提示：把练习7循环执行10次
*/
for($j=1; $j<=10; $j++){
	echo '※';
}
echo '<br>';
echo '<hr>';
/*
练习9)：输出5行10列个※
  提示：把练习8循环执行5次  —— 循环的嵌套
*/
for($i=1; $i<=5; $i++){
	for($j=1; $j<=10; $j++){
		echo '※';
	}
	echo '<br>';
}

echo '<hr>';
/*
练习10)：输出：
  ※						
  ※※					
  ※※※					
  ※※※※					
  ※※※※※				
*/
for($i=1; $i<=5; $i++){
	for($j=1; $j<=$i; $j++){
		echo '※';
	}
	echo '<br>';
}
