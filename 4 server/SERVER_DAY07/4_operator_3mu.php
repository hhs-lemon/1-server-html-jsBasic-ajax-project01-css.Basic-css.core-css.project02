*/
$score = 6500;
$result = $score>=5000 ? '白金用户' : (
	$score>=3000 ? '黄金用户' : (
		$score>=1000 ? '白银用户' : (
			$score>=500 ? '青铜用户' : '普通用户'
		)
	)
);
echo '<hr>';
