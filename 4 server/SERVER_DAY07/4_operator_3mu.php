<?php
/*
三目运算符可以嵌套：
变量保存学生成绩，输出：优秀、及格、不及格
*/
$score = 35;
$result = $score>=80 ? '优秀' : (
	$score>=60 ? '及格' : '不及格'
);
echo "成绩所在等级：$result";
echo '<hr>';

/*
